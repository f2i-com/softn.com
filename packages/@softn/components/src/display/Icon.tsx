export function Icon({
  name,
  svg,
  size = 20,
  color = 'currentColor',
  className,
  style,
}: IconProps): React.ReactElement {
  const pxSize = typeof size === 'number' ? `${size}px` : size;

  const wrapperStyle: React.CSSProperties = {
    display: 'inline-flex',
    alignItems: 'center',
    justifyContent: 'center',
    width: pxSize,
    height: pxSize,
    flexShrink: 0,
    ...style,
  };

  // Custom SVG markup
  if (svg) {
    return (
      <span
        className={className}
        style={wrapperStyle}
        dangerouslySetInnerHTML={{ __html: svg }}
      />
    );
  }

  // Built-in icon
  const iconSvg = name ? ICONS[name] : null;
  if (!iconSvg) {
    return <span className={className} style={wrapperStyle} />;
  }

  // Determine if the icon uses fill or stroke
  const usesFill = iconSvg.includes('fill=') || name === 'send' || name === 'star' || name === 'heart';

  return (
    <span className={className} style={wrapperStyle}>
      <svg
        xmlns="http://www.w3.org/2000/svg"
        width={pxSize}
        height={pxSize}
        viewBox="0 0 24 24"
        fill={usesFill ? color : 'none'}
        stroke={usesFill ? 'none' : color}
        strokeWidth={usesFill ? undefined : 2}
        strokeLinecap="round"
        strokeLinejoin="round"
        dangerouslySetInnerHTML={{ __html: iconSvg }}
      />
    </span>
  );
}

export default Icon;
